package oawc.getTemp;

public class TempVo {
	String date;
	String time;
	String temp;
	String DONAM;
	String DOCOD;
	String GUNNAM;
	String GUNCOD;
	  
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTemp() {
		return temp;
	}
	public void setTemp(String temp) {
		this.temp = temp;
	}
	public String getDONAM() {
		return DONAM;
	}
	public void setDONAM(String dONAM) {
		DONAM = dONAM;
	}
	public String getDOCOD() {
		return DOCOD;
	}
	public void setDOCOD(String dOCOD) {
		DOCOD = dOCOD;
	}
	public String getGUNNAM() {
		return GUNNAM;
	}
	public void setGUNNAM(String gUNNAM) {
		GUNNAM = gUNNAM;
	}
	public String getGUNCOD() {
		return GUNCOD;
	}
	public void setGUNCOD(String gUNCOD) {
		GUNCOD = gUNCOD;
	}
	
	
}
